const mongoose = require('mongoose');
var contact = mongoose.Schema({

    firstname :{
        type:String,
        required:true
    },
    lastname:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    phone:{
        type:String,
        required:true
    },
    message:{
        type:String,
        required:true
    }



})


module.exports = mongoose.model('contact',contact);